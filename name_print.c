void make_capital(int size, char **word){
    for(int i = 0; i < size; i++){
        if((*word)[i] >= 97){
            (*word)[i] -= 32;
        }
    }
}
void print_space(){
    printf("  ");
}
void print(int size, char *word){
    for(int i = 0; i < 5; i++){
        for(int j = 0; j < size; j++){
            for(int k = 0; k < 5; k++){
                printf("%c", letters[word[j] - 65][i][k]);
            }
            print_space();
        }
        printf("\n");
    }
}
